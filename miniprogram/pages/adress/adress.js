  addAddress:function(){
    var that = this;
    if(that.data.name.length == 0){
        wx.showToast({
            title: '收货人不能为空',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.phoneNum.length == 0){
        wx.showToast({
            title: '手机号不能为空',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.zipCode.length == 0){
        wx.showToast({
            title: '邮编不能为空',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.pId.length == 0){
        wx.showToast({
            title: '请选所在省份',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.cId.length == 0){
        wx.showToast({
            title: '请选择所在市',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.dId.length == 0){
        wx.showToast({
            title: '请选择所在区县',
            icon: 'loading',
            mask: true
        })
    }else if(that.data.detailAddress.length == 0){
        wx.showToast({
            title: '详细地址不能为空为空',
            icon: 'loading',
            mask: true
        })
    }else{
        request.req(uri_save_address, {
            trueName: that.data.name,
            mobPhone: that.data.phoneNum,
            zipCode: that.data.zipCode,
            provinceId: that.data.pId,
            cityId: that.data.cId,
            areaId: that.data.dId,
            areaInfo:that.data.pName+','+ that.data.cName+','+ that.data.dName,
            address: that.data.detailAddress,
        }, (err, res) => {
            var result = res.data;
            if (result.result == 1) { 
            wx.navigateBack({
                delta: 1, 
                success: function(res){
                // success
                },
                fail: function() {
                // fail
                },
                complete: function() {
                // complete
                }
            })
            } else {
                wx.showToast({
                    title: '保存失败',
                    icon: 'loading',
                    duration: 1500
                })
            }
        })
    }
  },
